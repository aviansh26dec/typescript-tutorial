"use strict";
var firstNum = 20;
var secondNum = 2;
// Addition
console.log("Addition");
console.log(firstNum + secondNum);
// Subtraction
console.log("Subtraction");
console.log(firstNum - secondNum);
// Multiplication
console.log("Mutliplication");
console.log(firstNum * secondNum);
// Division
console.log("Division");
console.log(firstNum / secondNum);
// Modulus
console.log("Modulus");
console.log(firstNum % secondNum);
// increement operator
console.log("Increement Postfix");
var x = 0;
while (x < 10) {
    console.log(x);
    x++;
}
console.log("Increement Prefix");
var z = 0;
while (z < 10) {
    console.log(z);
    ++z;
}
// decreement operator
console.log("Decreement Postfix");
var y = 10;
while (y > 0) {
    console.log(y);
    y--;
}
console.log("Decreement Prefix");
var w = 10;
while (w > 0) {
    console.log(w);
    --w;
}
