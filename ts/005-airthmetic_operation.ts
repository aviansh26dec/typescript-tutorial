let firstNum : number = 20;
let secondNum : number = 2;

// Addition
console.log("Addition");
console.log(firstNum + secondNum);

// Subtraction
console.log("Subtraction");
console.log(firstNum - secondNum);

// Multiplication
console.log("Mutliplication");
console.log(firstNum * secondNum);

// Division
console.log("Division");
console.log(firstNum / secondNum);

// Modulus
console.log("Modulus");
console.log(firstNum % secondNum);

// increement operator
console.log("Increement Postfix");
var x : number = 0;
while(x < 10){
    console.log(x);
    x++;
}

console.log("Increement Prefix");
let z : number = 0;
while(z < 10){
    console.log(z);
    ++z;
}


// decreement operator
console.log("Decreement Postfix");
let y : number = 10;
while(y > 0){
    console.log(y);
    y--;
}

console.log("Decreement Prefix");
let w : number = 10;
while(w > 0){
    console.log(w);
    --w;
}